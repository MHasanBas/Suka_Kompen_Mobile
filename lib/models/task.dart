class Task {
  final String title;
  final String description;
  final String tugas;
  final bool status;
  final String date;
  final int time;
  final String type;
  final String nama;
  final String jenis;

  Task(
      {required this.title,
      required this.description,
      required this.tugas,
      required this.status,
      required this.date,
      required this.time,
      required this.type,
      required this.nama,
      required this.jenis});
}